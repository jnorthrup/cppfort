// selfhost_pipeline_test.cpp - TDD test for closed-loop self-hosting pipeline
// Tests: .cpp2 → parse → canonical AST → emit C++ → compile → run
//
// Each test verifies one feature of the minimum C++2 subset needed for self-hosting.
// Tests are incremental: each builds on the previous working set.

#include <cassert>
#include <cstdlib>
#include <fstream>
#include <iostream>
#include <sstream>
#include <string>
#include <filesystem>

#include "bbcursive.cpp"

#ifndef CPPFORT_SOURCE_DIR
#define CPPFORT_SOURCE_DIR "."
#endif

// Helper: write source to temp file, parse it, return canonical AST
struct parse_outcome {
    bool parsed;
    std::vector<canonical_node> ast;
    std::string source;
};

static parse_outcome parse_file(const std::string& source_text) {
    parse_outcome result{};
    result.source = source_text;
    auto ast_opt = parse_source(std::string_view(result.source));
    if (ast_opt.has_value()) {
        result.parsed = true;
        result.ast = ast_opt.value();
    }
    return result;
}

// Helper: emit C++ from AST and source
static std::string emit_cpp(const std::vector<canonical_node>& ast, const std::string& source) {
    std::string output = "// Generated by cppfort from canonical AST\n#include <iostream>\n\n";
    int tag_count = 0;
    for (const auto& node : ast) {
        if (node.semantic == "bootstrap_tag_declaration") {
            auto start = static_cast<size_t>(node.source_start);
            auto stop = static_cast<size_t>(node.source_stop);
            if (start >= source.size() || stop > source.size()) continue;
            std::string_view decl = source.substr(start, stop - start);
            auto colon = decl.find(':');
            if (colon == std::string_view::npos) continue;
            std::string name(decl.substr(0, colon));
            while (!name.empty() && name.back() == ' ') name.pop_back();
            auto eq = decl.find('=', colon);
            if (eq == std::string_view::npos) continue;
            std::string_view val = decl.substr(eq + 1);
            while (!val.empty() && val.front() == ' ') val.remove_prefix(1);
            while (!val.empty() && (val.back() == ' ' || val.back() == ';')) val.remove_suffix(1);
            output += "constexpr int " + name + " = " + std::string(val) + ";\n";
            tag_count++;
        }
    }
    output += "\nint main() {\n    std::cout << \"cppfort: " + std::to_string(tag_count) + " tags\\n\";\n    return 0;\n}\n";
    return output;
}

// Helper: compile C++ source to executable, return exit code
static int compile_and_run(const std::string& cpp_code, const std::string& test_name) {
    std::string cpp_path = "/tmp/cppfort_tdd_" + test_name + ".cpp";
    std::string exe_path = "/tmp/cppfort_tdd_" + test_name;

    {
        std::ofstream out(cpp_path);
        out << cpp_code;
    }

    std::string compile_cmd = "clang++ -std=c++20 -o " + exe_path + " " + cpp_path + " 2>/dev/null";
    int compile_result = std::system(compile_cmd.c_str());
    if (compile_result != 0) {
        return -1; // compile failure
    }

    std::string run_cmd = exe_path + " 2>/dev/null";
    return std::system(run_cmd.c_str());
}

// ============================================================================
// TDD Test 1: Parser accepts struct declaration
// ============================================================================
static bool test_parser_accepts_struct() {
    constexpr auto source = "point: @struct type = {\n    x: i32 = 0;\n    y: i32 = 0;\n}\n";
    auto result = parse_file(source);

    if (!result.parsed) {
        std::cerr << "TDD FAIL [parser_struct]: parser rejected struct declaration\n";
        return false;
    }

    // Verify struct_declaration feature is present
    bool found_struct = false;
    for (const auto& f : result.session.features) {
        if (f.semantic == "struct_declaration") {
            found_struct = true;
            break;
        }
    }
    if (!found_struct) {
        std::cerr << "TDD FAIL [parser_struct]: no struct_declaration feature found\n";
        return false;
    }

    std::cerr << "TDD PASS [parser_struct]: struct declaration parsed\n";
    return true;
}

