// ============================================================================
// Main test runner
// ============================================================================
int main() {
    int passed = 0;
    int total = 0;

    #define RUN_TEST(fn) do { total++; if (fn()) passed++; } while(0)

    // Layer 1: Parser acceptance (must pass first)
    RUN_TEST(test_parser_accepts_struct);
    RUN_TEST(test_parser_accepts_enum);
    RUN_TEST(test_parser_accepts_flag_enum);
    RUN_TEST(test_parser_accepts_function);

    // Layer 2: Canonical AST construction
    RUN_TEST(test_canonical_ast_has_struct_nodes);

    // Layer 3: Emitter produces compilable output
    RUN_TEST(test_emitter_compilable_struct);

    // Layer 4: Full round-trip
    RUN_TEST(test_roundtrip_tag_declaration);

    // Layer 5: Multi-declaration and selfhost patterns
    RUN_TEST(test_parser_multiple_declarations);
    RUN_TEST(test_rbcursive_header_parses);
    RUN_TEST(test_generic_struct);

    #undef RUN_TEST

    std::cerr << "\n=== TDD Results: " << passed << "/" << total << " passed ===\n";

    if (passed == total) {
        std::cerr << "SUCCESS: All selfhost pipeline tests passed!\n";
        return 0;
    } else {
        std::cerr << "FAILURE: " << (total - passed) << " test(s) failed\n";
        return 1;
    }
}
