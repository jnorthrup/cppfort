[[nodiscard]] auto extract_tag_name(cpp2::impl::in<std::string> semantic) -> std::string{
    std::size_t colon_pos {CPP2_UFCS(find)(semantic, ':')}; 
    if (colon_pos == std::string::npos) {
        return ""; 
    }

    std::string name {CPP2_UFCS(substr)(semantic, 0, cpp2::move(colon_pos))}; 
    std::size_t start {CPP2_UFCS(find_first_not_of)(name, " \t")}; 
    std::size_t end {CPP2_UFCS(find_last_not_of)(name, " \t")}; 
    if (start != std::string::npos && end != std::string::npos) {
        name = CPP2_UFCS(substr)(name, start, cpp2::move(end) - start + 1);
    }

    return name; 
}

