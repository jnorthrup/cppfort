[[nodiscard]] auto emit_cpp_from_canonical(
    cpp2::impl::in<std::vector<canonical_node>> nodes, 
    std::string& output
) -> bool{
    output = "// Generated by cppfort from canonical AST\n";
    output += "// Minimal dogfood bootstrap\n\n";
    output += "#include <iostream>\n\n";

    bool has_tags {false}; 

    int i {0}; 
    int fsz {cpp2::unchecked_narrow<int>(CPP2_UFCS(size)(nodes))}; 
    while( cpp2::impl::cmp_less(i,fsz) ) {
        canonical_node node {CPP2_ASSERT_IN_BOUNDS(nodes, cpp2::unchecked_narrow<std::size_t>(i))}; 

        // Tag 21 = canonical_normalize_tag (bootstrap tag declarations)
        if (node.tag == 21) {
            emit_tag_declaration(cpp2::move(node).semantic, output);
            has_tags = true;
        }

        i += 1;
    }

    if (cpp2::move(has_tags)) {
        output += "\nint main() {\n";
        output += "    std::cout << \"cppfort bootstrap: \" << ";

        bool first {true}; 
        i = 0;
        while( cpp2::impl::cmp_less(i,fsz) ) {
            canonical_node node {CPP2_ASSERT_IN_BOUNDS(nodes, cpp2::unchecked_narrow<std::size_t>(i))}; 
            if (node.tag == 21) {
                std::string tag_name {extract_tag_name(cpp2::move(node).semantic)}; 
                if (!(CPP2_UFCS(empty)(tag_name))) {
                    if (!(first)) {
                        output += " << \", \" << ";
                    }
                    output += tag_name;
                    output += "=";
                    output += cpp2::move(tag_name);
                    first = false;
                }
            }
            i += 1;
        }

        output += " << \"\\n\";\n";
        output += "    return 0;\n";
        output += "}\n";
    }else {
        output += "\nint main() {\n";
        output += "    std::cout << \"cppfort: no tags found\\n\";\n";
        output += "    return 0;\n";
        output += "}\n";
    }

    return true; 
}

